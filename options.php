        <div id="lang302" class="faqHeader">Options Premium? What is it?</div>
        <div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a id="lang303" class="accordion-toggle collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapse17">Definition</a>
                </h4>
            </div>
            <div id="collapse17" class="panel-collapse collapse">
                <div id="lang304" class="panel-body">
                    The 'options premium' is the price paid to acquire the option, also known simply as the 'option price'. </p>The premium is not to be confused with the option’s strike price. Market price, asset volatility and time remaining till expiration are the primary forces determining the premium. </p>There are two components to the options premium: intrinsic value and time value.</p>
                </div>
            </div>
        </div>

        <div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a id="lang305" class="accordion-toggle collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapse18">Intrinsic Value</a>
                </h4>
            </div>
            <div id="collapse18" class="panel-collapse collapse">
                <div id="lang306" class="panel-body">
                    The intrinsic value is determined by the difference between the current trading price and the strike price.</p>Only in-the-money options have intrinsic value. Intrinsic value can be computed for in-the-money options by taking the difference between the strike price and the current trading price. </p>Out-of-the-money options have no intrinsic value.</p>In the pizza coupon example, the difference between the cost of acquiring the coupon and the pizza price is the intrinsic value.</p>
                </div>
            </div>
        </div>

		<div class="panel panel-default">
            <div class="panel-heading">
                <h4 class="panel-title">
                    <a id="lang307" class="accordion-toggle collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapse19">Time Value</a>
                </h4>
            </div>
            <div id="collapse19" class="panel-collapse collapse">
                <div id="lang308" class="panel-body">
                An option's time value is dependent upon the length of time remaining to exercise the option, the current market value of the option, as well as the volatility of the underlying Bitcoin price.</p>The time value of an option decreases as its expiration date approaches and becomes worthless after that date. </p></p>This phenomenon is known as time decay. For in-the-money options, time value can be calculated by subtracting the intrinsic value from the option price. </p>Time value decreases as the option goes deeper into the money.</p></p>For out-of-the-money options, since there is zero intrinsic value, time value equals the option price.</p>Typically, higher volatility gives rise to higher time value. In general, time value increases as the uncertainty of the option's value at expiry increases.</p></p>
