                </h4>
            </div>
            <div id="collapse98" class="panel-collapse collapse">
                <div id="lang263" class="panel-body">
					A situation where an option's strike price is identical to the price of the underlying security.</p> Both call and put options will be simultaneously 'at the money.' </p>For example, if Bitcoin is trading at 200, then the Bitcoin 200 call option is at the money and so is the Bitcoin 200 put option. </p>An at-the-money option has no intrinsic value, but may still have time value. Options trading activity tends to be high when options are at the money.</p>
                </div>
            </div>
        </div>

